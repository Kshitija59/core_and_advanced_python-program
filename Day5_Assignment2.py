"""2. Declare a square() function with one parameter.
Then call the function and pass one number and display the square of that number ."""


# Define the square function that takes one parameter (n)
def square(n):
    # Calculate the square of the number and return the result
    return n * n

# Call the function with one number and store the result
result = square(20)

# Display the result
print("The square of the number is:", result)



#Output
"The square of the number is: 400"

